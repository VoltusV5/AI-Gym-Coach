# Коэффициенты модификации
WEIGHT_MODIFIERS = {
    "новичок":    0.70,
    "любитель":   1.00,
    "продвинутый":1.25,
    "junior":     0.65,   # <18 лет
    "middle":     0.90,
    "senior":     0.60,   # ≥60 лет
}

def get_initial_weight(ex_name: str, пол: str, уровень: str, возраст: int, травмы: bool) -> int | float:
    base = INITIAL_WEIGHTS_KG.get(пол, INITIAL_WEIGHTS_KG["м"]).get(ex_name, 0)
    if base == 0:
        return 0  # собственный вес или тело

    mod = WEIGHT_MODIFIERS.get(уровень, 1.0)
    age_group = get_age_group(возраст)
    mod *= WEIGHT_MODIFIERS.get(age_group, 1.0)

    if травмы:
        mod *= 0.65

    # Округляем до 2.5 кг (типичный шаг блинов / гантелей)
    weight = round(base * mod / 2.5) * 2.5
    return max(2.5, weight) if weight > 0 else 0


# ========== ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ ==========

def get_valid_variations(main, variations):
    main_key = main.split()[0]

    for key in VALID_VARIATIONS:
        if key in main_key:
            return [
                v for v in variations
                if any(valid in v for valid in VALID_VARIATIONS[key])
            ][:2]

    return variations[:2]


def get_age_group(age: int) -> str:
    """Определяет возрастную группу"""
    if age < 18:
        return "junior"
    elif age < 40:
        return "adult"
    elif age < 60:
        return "middle"
    else:
        return "senior"

def is_safe_for_injury(exercise_name: str, травмы: bool) -> bool:
    """Проверяет, безопасно ли упражнение при травмах"""
    if not травмы:
        return True
    
    # Список опасных упражнений при травмах
    dangerous = [
        "приседания со штангой",
        "румынская тяга",
        "подтягивания",
        "отжимания на брусьях",
        "тяга штанги в наклоне"
    ]
    
    # Если упражнение опасное, а у человека травмы — не рекомендуем
    if exercise_name in dangerous:
        return False
    
    return True

def pick_structured_exercises(muscle: str, травмы: bool, age: int):
    """Подбирает упражнения с учётом возраста и травм"""
    base, secondary, isolation = [], [], []
    
    age_group = get_age_group(age)
    
    for ex_type in EXERCISES.get(muscle, {}):
        for ex in EXERCISES[muscle][ex_type]:
            role = EXERCISE_ROLE.get(ex["name"], "isolation")
            
            # Фильтруем опасные упражнения при травмах
            if not is_safe_for_injury(ex["name"], травмы):
                continue
                
            # Для пожилых людей убираем слишком сложные упражнения
            if age_group == "senior" and ex["name"] in ["подтягивания", "отжимания на брусьях", "приседания со штангой"]:
                continue
            
            if role == "base":
                base.append(ex)
            elif role == "secondary":
                secondary.append(ex)
            else:
                isolation.append(ex)

    chosen = []
    
    # Баланс: всегда стараемся дать хотя бы 2 упражнения, но безопасные
    if травмы or age_group == "senior":
        # Для травм и пожилых: больше изоляции, меньше базы
        if secondary and random.random() > 0.3:
            chosen.append(random.choice(secondary))
        if isolation:
            chosen.append(random.choice(isolation))
    else:
        # Для здоровых: классическая пирамида
        if base:
            chosen.append(random.choice(base))
        if secondary:
            chosen.append(random.choice(secondary))
        if isolation:
            chosen.append(random.choice(isolation))
    
    # Убираем дубликаты
    seen = set()
    unique_chosen = []
    for ex in chosen:
        if ex["name"] not in seen:
            seen.add(ex["name"])
            unique_chosen.append(ex)
    
    return unique_chosen

def adjust_by_level(exercises, level, травмы, age):
    if травмы or get_age_group(age) == "senior":
        return exercises[:random.choice([1,2])]

    if level == "новичок":
        return exercises[:random.choice([1,2])]

    elif level == "любитель":
        return exercises[:random.choice([2,3])]

    return exercises[:random.choice([2,3])]

def get_sets_count(level, травмы, age):
    """Определяет количество подходов"""
    if травмы or get_age_group(age) == "senior":
        return 3
    return 3 if level != "продвинутый" else 4

def get_reps_range(goal, age):
    base = {
        "Сжечь жир": ["12-15", "15-20"],
        "Скинуть вес": ["12-15", "15-20"],
        "Набрать мышцы": ["6-10", "8-12"]
    }.get(goal, ["8-12"])

    if get_age_group(age) == "senior":
        return "15-20"

    return random.choice(base)

def get_rest(level, травмы, age):
    """Определяет время отдыха"""
    base_rest = {
        "новичок": "4-6 мин",
        "любитель": "4 мин",
        "продвинутый": "3 мин"
    }.get(level, "1.5 мин")
    
    if get_age_group(age) == "senior":
        return "4 мин"
    
    return base_rest

# ========== ОСНОВНАЯ ФУНКЦИЯ ==========

def generate_workout(user_input: Dict) -> Dict:
    """
    Генерирует тренировку на основе данных пользователя
    """
    цель = user_input.get("цель", "Набрать мышцы")
    уровень = user_input.get("уровень_подготовки", "новичок")
    травмы = user_input.get("травмы_или_болезни", "нет") == "да"
    возраст = user_input.get("возраст", 30)


    дни = user_input.get("дни_тренировок", [])
    кол_дней = max(2, min(4, len(дни))) if isinstance(дни, list) else дни

    сплит = SPLITS_BY_DAYS.get(кол_дней, SPLITS_BY_DAYS[3])

    план = {}

    for day_name, muscle_groups in сплит.items():
        day_plan = []

        for muscle in muscle_groups:
            exercises = []

            structured = pick_structured_exercises(muscle, травмы, возраст)

            for ex in structured:
                exercises.append({
                    "основное": ex["name"],
                    "вариации": get_valid_variations(ex["name"], ex["вариации"])
                })

            exercises = adjust_by_level(exercises, уровень, травмы, возраст)

            day_plan.append({
                "группа": muscle,
                "упражнения": exercises,
                "подходы": get_sets_count(уровень, травмы, возраст),
                "повторения": get_reps_range(цель, возраст),
                "отдых": get_rest(уровень, травмы, возраст)
            })

        план[day_name] = day_plan

    # ✅ Генерация весов после полного плана
    начальные_веса = {}

    for groups in план.values():
        for group in groups:
            for ex in group["упражнения"]:
                основное = ex["основное"]
                вес = get_initial_weight(
                    ex_name=основное,
                    пол=user_input.get("пол", "м"),
                    уровень=уровень,
                    возраст=возраст,
                    травмы=травмы
                )
                начальные_веса[основное] = вес

    return {
        "план_тренировок": план,
        "начальные_веса": начальные_веса
    }


