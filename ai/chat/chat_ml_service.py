"""
Локальный ML-сервис для backend: POST /ai/user/chat → text/plain потоковый или статический ответ.
Запуск из папки ai:  python -m uvicorn chat.chat_ml_service:app --host 127.0.0.1 --port 8000
"""

from fastapi import Body, FastAPI
from fastapi.responses import Response, StreamingResponse
from pydantic import BaseModel
from cerebras.cloud.sdk import Cerebras
from dotenv import load_dotenv
from typing import Any, Dict, Optional
import os
try:
    from g4f.client import Client as G4FClient
except ImportError:
    G4FClient = None


def prompt(chat: list[dict], user, plan) -> list[dict]:
    """
    Функция создающая промпт для нейросети и задающая её поведение.
    """
    system = [{
        "role": "system",
        "content": f"""
        Ты AI фитнес-ассистент и профессиональный тренер.

        Правила:
        - Не давай медицинских диагнозов.
        - Не советуй опасные упражнения при наличии ограничений.
        - Отвечай кратко, структурировано, поддерживай дружелюбный и мотивирующий тон.
        - Если пользователь новичок, предлагай простые базовые упражнения.

        Вот параметры спортсмена: {user}
        Вот его тренировочный план: {plan}
        """
    }]

    # Берем последние 10 сообщений из истории диалога
    return system + chat[-10:]

class ChatRequest(BaseModel):
    messages: list
    user: dict
    plan: Optional[dict] = None

load_dotenv()
api_key = os.getenv("CEREBRAS_API_KEY")
client = Cerebras(api_key=api_key) if api_key else None

app = FastAPI(title="sportik-ml-chat", version="0.1.0")

@app.post("/ai/user/chat")
async def ai_chat(chat: ChatRequest):
    last_user = chat.messages[-1]['content'] if chat.messages else ""
    query = last_user.lower()
    
    # Извлекаем параметры для глубокой персонализации
    user_p = chat.user if chat.user else {}
    weight = user_p.get("weight", "не указан")
    goal = user_p.get("goal", "поддержание формы")
    level = user_p.get("experience_level", "начинающий")

    # Функция локальной генерации экспертного ответа
    def get_expert_response() -> str:
        # 1. Замена упражнений
        if any(w in query for w in ["заменит", "замена", "вместо", "альтернатив", "нет тренажер", "болит колен", "болит плеч"]):
            return (
                f"🔄 **Подбор альтернативного упражнения**\n\n"
                f"По поводу вашего запроса («{last_user}»):\n\n"
                f"Если упражнение вызывает дискомфорт или недоступен нужный снаряд, вот отличные и безопасные замены:\n"
                f"- **Для ног и ягодиц:** Жим ногами в платформе, болгарские сплит-приседания с гантелями или кубковые приседания (Goblet Squat).\n"
                f"- **Для грудных / плеч:** Отжимания от пола (с колен при необходимости) или жим гантелей лежа под углом — это снимает лишнюю нагрузку с суставов.\n"
                f"- **Для спины:** Тяга гантели в наклоне с упором или вертикальная тяга верхнего блока.\n\n"
                f"💡 *Совет тренера:* Начинайте новое упражнение с минимального разминочного веса, чтобы прочувствовать биомеханику движения без боли."
            )
            
        # 2. Подбор весов и прогрессия
        if any(w in query for w in ["вес", "сколько брать", "нагрузк", "прогресс", "увеличивать", "гантел", "штанга"]):
            return (
                f"⚖️ **Как правильно подобрать рабочий вес**\n\n"
                f"Отвечаю на ваш вопрос («{last_user}»):\n\n"
                f"Ваша текущая цель — **{goal}** (уровень: {level}). Оптимальный вес подбирается по следующему принципу:\n"
                f"1. **Правило последних повторений:** Вес должен быть таким, чтобы последние 2 повторения в подходе давались с ощутимым трудом, но **без нарушения техники**.\n"
                f"2. **Прогрессия нагрузок:** Если вы уверенно выполняете все заданные повторения (например, 3 подхода по 12 раз), на следующей тренировке увеличьте вес на 2.5–5 кг.\n"
                f"3. **Разминка:** Всегда делайте 1-2 подводящих подхода с весом 50% и 70% от рабочего перед основной сессией.\n\n"
                f"🚀 Главное — не торопитесь за весами. Чистая техника дает гораздо лучший стимул для роста мышц."
            )

        # 3. Техника и дыхание
        if any(w in query for w in ["техник", "дыхан", "дышать", "как делать", "правильно", "спина", "поясниц"]):
            return (
                f"🫁 **Техника выполнения и контроль дыхания**\n\n"
                f"Касательно вашего вопроса («{last_user}»):\n\n"
                f"Золотое правило безопасного тренинга:\n"
                f"• **Дыхание:** Вдох всегда делается в негативной фазе (когда вы опускаете вес или растягиваете мышцу). Мощный **выдох** — на максимальном усилии (при подъеме или жиме).\n"
                f"• **Положение корпуса:** Держите спину нейтральной, сохраняя естественный прогиб в пояснице. Лопатки должны быть сведены и опущены вниз, особенно при жимах и тягах.\n"
                f"• **Темп:** Старайтесь опускать вес за 2-3 секунды, а поднимать мощно за 1 секунду. Не бросайте снаряд вниз."
            )

        # 4. Питание и добавки
        if any(w in query for w in ["питан", "еда", "кушать", "протеин", "креатин", "добавк", "пить", "вода", "сушка", "похуден", "масс"]):
            return (
                f"🥗 **Рекомендации по питанию и нутриентам**\n\n"
                f"Отличный вопрос про рацион («{last_user}»):\n\n"
                f"Для достижения вашей цели ({goal}) соблюдайте следующие основы:\n"
                f"- **До тренировки (за 1.5–2 часа):** Сложные углеводы + нежирный белок (гречка/рис с курицей или овсянка), чтобы обеспечить организм энергией.\n"
                f"- **После тренировки:** Полноценный прием пищи в течение часа (белок для восстановления мышц + углеводы для восполнения гликогена).\n"
                f"- **Гидратация:** Пейте по 150–200 мл чистой воды каждые 15–20 минут. Обезвоживание снижает силовые показатели на 15%!\n"
                f"- **Спортивное питание:** Протеин удобен для быстрого добора белка, а креатин (5 г в день) поможет увеличить силовую выносливость."
            )

        # 5. Разминка, заминка, боль в мышцах
        if any(w in query for w in ["разминк", "растяжк", "заминк", "болят мышцы", "крепатур", "восстановлен", "сон"]):
            return (
                f"🧘 **Разминка и восстановление мышц**\n\n"
                f"По поводу вашего вопроса («{last_user}»):\n\n"
                f"• **Разминка (Перед стартом):** 5-10 минут кардио до легкой испарины + динамические вращения суставов. Это увеличивает выработку синовиальной жидкости и защищает от травм.\n"
                f"• **Заминка и растяжка (После сессии):** Статическая растяжка целевых мышц по 20-30 секунд. Она снимает гипертонус и ускоряет выведение продуктов обмена.\n"
                f"• **Мышечная боль:** Легкая боль на 1-2 день — это норма. Лучшее средство для восстановления: качественный сон (7-8 часов) и легкая активность для улучшения кровообращения."
            )

        # 6. Программа, подходы, количество повторений
        if any(w in query for w in ["подход", "повторен", "программ", "план", "сколько раз", "тренировк"]):
            return (
                f"📋 **Организация тренировочного процесса**\n\n"
                f"Разбираем ваш запрос («{last_user}»):\n\n"
                f"В вашем плане предусмотрена сбалансированная нагрузка. Стандартные ориентиры:\n"
                f"- **Количество подходов:** 3-4 рабочих подхода на каждое упражнение дают оптимальный объем для стимуляции мышечных волокон.\n"
                f"- **Диапазон повторений:**\n"
                f"  • *Для силы:* 5–8 повторений с тяжелым весом и отдыхом 2–3 минуты.\n"
                f"  • *Для гипертрофии (роста мышц):* 8–12 повторений с отдыхом 1.5–2 минуты.\n"
                f"  • *Для рельефа:* 15–20 повторений с коротким отдыхом (45–60 секунд).\n\n"
                f"Слушайте свое тело: если чувствуете сильное утомление, лучше сделать на один подход меньше, но качественно!"
            )

        # 7. Приветствие
        if any(w in query for w in ["привет", "здравствуй", "как дела", "кто ты", "ты кто"]):
            return (
                f"👋 Привет! Я ваш персональный AI-тренер Sportik.\n\n"
                f"Готов помочь вам с любым вопросом. Вы можете спросить меня:\n"
                f"• Как правильно подобрать рабочий вес\n"
                f"• Чем заменить упражнение, если нет тренажера\n"
                f"• Как правильно дышать во время подходов\n"
                f"• Что съесть перед тренировкой\n\n"
                f"Напишите свой вопрос, и мы разберем его детально!"
            )

        # 8. Динамический ответ по умолчанию, напрямую отвечающий на текст пользователя
        return (
            f"Отличный вопрос! Давайте детально разберем ваш запрос: **«{last_user}»**.\n\n"
            f"Исходя из вашего профиля (Цель: **{goal}**, Уровень: **{level}**), я рекомендую подходить к этому вопросу индивидуально:\n\n"
            f"📌 **Главный акцент:**\n"
            f"В тренировочном процессе ключевую роль играет правильная техника. Старайтесь ориентироваться на ощущения в целевых мышцах и не допускать работы «в отказ» при появлении дискомфорта в суставах.\n\n"
            f"⚖️ **Практический совет:**\n"
            f"Попробуйте протестировать этот подход на ближайшей тренировке в рамках первых двух рабочих подходов. Запишите ощущения в заметки к сессии — это поможет отследить прогресс.\n\n"
            f"Если вам нужно подобрать конкретную замену упражнения, рассчитать рабочие веса или уточнить нюансы по питанию — просто напишите ключевое слово (например, *«замена»*, *«вес»* или *«питание»*)!"
        )

    fallback_msg = get_expert_response()

    # 1. Если задан официальный ключ Cerebras, пробуем получить ответ через него
    if client:
        try:
            response = client.chat.completions.create(
                messages=prompt(chat.messages, chat.user, chat.plan),
                model="llama3.1-8b",
                max_completion_tokens=1024,
                temperature=0.2,
                top_p=1,
                stream=True
            )
            def event_stream():
                for chunk in response:
                    content = chunk.choices[0].delta.content
                    if content:
                        yield content
            return StreamingResponse(event_stream(), media_type="text/plain; charset=utf-8")
        except Exception as e:
            print(f"[ml-chat] Cerebras API error / Cloudflare block: {e}")

    # 2. Если официальный API недоступен или не настроен, задействуем бесплатную живую нейросеть через G4F
    if G4FClient:
        try:
            g4f_client = G4FClient()
            g4f_response = g4f_client.chat.completions.create(
                model="gpt-4o-mini",
                messages=prompt(chat.messages, chat.user, chat.plan),
                stream=True
            )
            def g4f_stream():
                for chunk in g4f_response:
                    content = chunk.choices[0].delta.content
                    if content:
                        yield content
            return StreamingResponse(g4f_stream(), media_type="text/plain; charset=utf-8")
        except Exception as g4f_e:
            print(f"[ml-chat] G4F free provider error: {g4f_e}")

    # 3. Финальный уровень защиты: наш мощный локальный эвристический экспертный движок
    return Response(content=fallback_msg, media_type="text/plain; charset=utf-8")


